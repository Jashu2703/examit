def get_exam_config(exam_type: str) -> dict:
    return SYLLABUS.get(exam_type, {})

def get_subjects(exam_type: str) -> list:
    config = get_exam_config(exam_type)
    return list(config.get("subjects", {}).keys())

def get_chapters(exam_type: str, subject: str) -> list:
    config = get_exam_config(exam_type)
    return config.get("subjects", {}).get(subject, [])

def get_all_topics(exam_type: str) -> list:
    config = get_exam_config(exam_type)
    topics = []
    for subject, chapters in config.get("subjects", {}).items():
        for chapter in chapters:
            topics.append({"subject": subject, "chapter": chapter})
    return topics
